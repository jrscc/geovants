package GeoVANT.model;

import java.util.ArrayList;

public class SensorValueMapping {
	
	private String mappedCapabilityField;
	private boolean mapCoordinatesAsArray;
	private String mappedLatitudeField;
	private String mappedLongitudeField;
	private ArrayList<String> mappedValuesField;
	private String mappedReadingNumberField;
	private String mappedTimestampField;
	private String mappedDateField;
	private String mappedTimeField;
	public String getMappedCapabilityField() {
		return mappedCapabilityField;
	}
	public void setMappedCapabilityField(String mappedCapabilityField) {
		this.mappedCapabilityField = mappedCapabilityField;
	}
	public boolean isMapCoordinatesAsArray() {
		return mapCoordinatesAsArray;
	}
	public void setMapCoordinatesAsArray(boolean mapCoordinatesAsArray) {
		this.mapCoordinatesAsArray = mapCoordinatesAsArray;
	}
	public String getMappedLatitudeField() {
		return mappedLatitudeField;
	}
	public void setMappedLatitudeField(String mappedLatitudeField) {
		this.mappedLatitudeField = mappedLatitudeField;
	}
	public String getMappedLongitudeField() {
		return mappedLongitudeField;
	}
	public void setMappedLongitudeField(String mappedLongitudeField) {
		this.mappedLongitudeField = mappedLongitudeField;
	}
	public ArrayList<String> getMappedValuesField() {
		return mappedValuesField;
	}
	public void setMappedValuesField(ArrayList<String> mappedValuesField) {
		this.mappedValuesField = mappedValuesField;
	}
	public String getMappedReadingNumberField() {
		return mappedReadingNumberField;
	}
	public void setMappedReadingNumberField(String mappedReadingNumberField) {
		this.mappedReadingNumberField = mappedReadingNumberField;
	}
	public String getMappedTimestampField() {
		return mappedTimestampField;
	}
	public void setMappedTimestampField(String mappedTimestampField) {
		this.mappedTimestampField = mappedTimestampField;
	}
	public String getMappedDateField() {
		return mappedDateField;
	}
	public void setMappedDateField(String mappedDateField) {
		this.mappedDateField = mappedDateField;
	}
	public String getMappedTimeField() {
		return mappedTimeField;
	}
	public void setMappedTimeField(String mappedTimeField) {
		this.mappedTimeField = mappedTimeField;
	}
	
	
}